ll pow(ll a, ll b) {
    ll ans = 1;
    while (b) {
        if (b & 1) ans *= a;
        b >>= 1;
        a *= a;
    }
    return ans;
}

ll pow(ll a, ll b, ll c) {
    ll ans = 1;
    while (b) {
        if (b & 1) ans = (ans * a) % c;
        b >>= 1;
        a = (a * a) % c;
    }
    return ans;
}

void solve() {
    // wannabe knapsack ahh
    int n; cin >> n;
    int max_sum = n*1000;
    vector<int> x(n); in(x);
    vector<vector<bool>> dp(n+1,vector<bool>(max_sum+1,false));
    dp[0][0] = true;
    for (int i = 1; i <= n; i++) {
        for (int j = 0; j <= max_sum; j++) {
            dp[i][j] = dp[i-1][j];
            int left = j-x[i-1];
            if (left >= 0 && dp[i-1][left]) {
                dp[i][j] = true;
            }
        }
    }
    vector<int> ans;
    for (int j = 1; j <= max_sum; j++) {
        if (dp[n][j]) {
            ans.push_back(j);
        }
    }
    cout << ans.size() << endl;
    out(ans);
}

